package TP_Grafos.Parcial2;

public class Nodo {
	private String letra;
	private boolean color;
	
	public String getLetra() {
		return letra;
	}
	public void setLetra(String letra) {
		this.letra = letra;
	}
	public boolean isColor() {
		return color;
	}
	public void setColor(boolean color) {
		this.color = color;
	}
}
