package TP_Grafos.Parcial3;

public class Ciudad {
	private String nombre;
	private int dias;
	
	public void setNombre (String nombre) {
		this.nombre = nombre;
	}
	
	public String getNombre () {
		return this.nombre;
	}
	
	public void setDias(int dias) {
		this.dias = dias;
	}
	
	public int getDias () {
		return this.dias;
	}
	
	
}
