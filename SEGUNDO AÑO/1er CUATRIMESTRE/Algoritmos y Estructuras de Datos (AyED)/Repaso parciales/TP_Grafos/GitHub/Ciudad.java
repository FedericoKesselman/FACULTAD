package TP_Grafos.GitHub;

public class Ciudad {
	private String nombre;
	private int fase;
	
	public void setNombre (String nombre) {
		this.nombre = nombre;
	}
	
	public String getNombre() {
		return this.nombre;
	}
	
	public void setFase (int fase) {
		this.fase = fase;
	}
	
	public int getFase() {
		return this.fase;
	}
}
